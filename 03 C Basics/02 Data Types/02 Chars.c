#include <stdio.h>

int main() {
    /*
    Characters or char
    - single characters
    - these are ASCII characters
    - literals are integers or use single quotes like 'A'
    - chars are 8 bits, or 1 byte
    */
   
    char letter = 'A';
    printf("letter = %c\n", letter);
    printf("letter = %d\n", letter);
    
    return 0;
}